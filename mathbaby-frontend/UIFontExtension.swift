import Foundation
import UIKit

extension UIFont {
    
    static var systemFontStandard:UIFont { get { return UIFont.systemFontOfSize(60) } }
    static var systemFontStandardBold:UIFont { get { return UIFont.boldSystemFontOfSize(60) } }
    
}